/**================================== NodeJS vs Java vs Python =====================================================
 * 
 *  NodeJS: 
 *    Type            - Javascript runtime built on chrome's V8 engine
 *    Execution model - Single threaded, Event driven, Non blocking IO(File reading, Http requests, database queries)
 *    Performance     - Very fast in IO heavy tasks due to non-blocking architecture and V8 engine
 *    Use case fit    - Real time apps like chat, live updates, APIs, Microservices
 *    Concurency      - Handles many simultaneous connections efficiently through thread pool where multiple worker runs
 * 
 * 
 *  JAVA: 
 *    Type            - Compiled, statically typed, Object oriented programming language
 *    Execution model - Multi-threaded model with built in concurrency and thread control
 *    Performance     - Excellent in handling heavy computational tasks(Cpu extensive task) due to multithreading model
 *    Use case fit    - Enterprise apps, Banking applications, telecom and finance etc, 
 *    Concurency      - Native multithreading with high load handling
 *     
 * 
 * 
 *  Python: 
 *    Type            - High level, Interpreted scripting language
 *    Execution model - Primarily synchronous but supports asynchronous approach
 *    Performance     - Slower in CUP intensive task better suited for AI/ML or prototyping
 *    Use case fit    - Rapid and Fast development due to simple plain english syntax, AI/ML apps 
 *    Concurency      - Acheived using multiprocessing or asyncio
 */





