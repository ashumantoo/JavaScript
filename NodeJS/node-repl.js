/******************************************************************************************************************
 *                                                    REPL IN NODEJS
 * TUTORIAL URL : https://www.digitalocean.com/community/tutorials/how-to-use-the-node-js-repl
 * 
 * --> The Node.js Read-Eval-Print-Loop (REPL) is an interactive shell that processes Node.js expressions.
 * --> READ - Reads Javascript code that user enters
 *     Eval - Evaluate the result of interpreting the line of code
 *     Print - Prints the results to the user
 *     Loop - Loops until the user signals to quit
 * 
 * Step 1 — Starting and Stopping the REPL
 *      --> If you have node installed, then you also have the Node.js REPL. To start it, simply enter node in your command line shell
 * Step 2 — Executing Code in the Node.js REPL
 *      --> A) Can call functions
 *          B) Can create variables
 *          C) Can add multiline blocks
 * 
 * Step 3 — Mastering REPL Shortcuts
 *      --> use UP and DOWN arrow to navigat to the prvious and next used code/command
 *      --> TAB - for autocomplete of Predefined Nodejs function/methods
 *      --> Math. - TAB twice to get all the possible autocomplete of math functions
 *      --> .help - To list all the available commands
 *      --> .break    Sometimes you get stuck, this gets you out
 *      --> .clear    Alias for .break
 *      --> .editor   Enter editor mode
 *      --> .exit     Exit the repl
 *      --> .help     Print this help message
 *      --> .load     Load JS from a file into the REPL session
 *      --> .save     Save all evaluated commands in this REPL session to a file
 */ 

